
#include<stdio.h>

int main()
{
	int a,b,c;
	printf("Enter value of a : ");
	scanf("%d", &a);
	printf("Enter value of b : ");
	scanf("%d", &b);
	printf("Enter value of c : ");
	scanf("%d", &c);

	printf("Sum of a, b, and c %d\n", a+b+c);
	printf("Substraction of a, b, and c %d\n", a-b-c);
	printf("Multiplication of a, b, and c %d\n", a*b*c);
	return 0;
}
